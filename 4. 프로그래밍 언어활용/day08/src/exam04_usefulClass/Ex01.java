package exam04_usefulClass;
import static java.lang.Math.*;
public class Ex01 {
    public static void main(String[] args) {
        System.out.println(abs(-10));
        System.out.println(ceil(10.2));
        System.out.println(floor(10.2));
        System.out.println(round(10.5));
        System.out.println(Math.max(10,5));
        System.out.println(Math.pow(11,2));
        System.out.println(Math.random());
        System.out.println((int)sqrt(12321));
        System.out.println(pow(sqrt(10),2));
    }
}
